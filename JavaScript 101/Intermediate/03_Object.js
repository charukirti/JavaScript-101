// ⭐ JavaScript Objects :- JavaScript object is a collection of a key-value pairs. Each collection of a key-pair is called as property.

// JavaScript has provides different ways to create an object. But the most common way to create an object is using object literal notation

// Syntax 👉 let myObj = {};

// to create object property we use key:value within the curly braces.

// Example 👉 let myObj = {myName : "Charukirti"};

//⭐ Accessing object properties

// 1. Dot notaion (.)

// Syntax 👉 objectName.propertyName;

// Example :

let user = {
  userName: "Charukirti",
  age: 20,
};

console.log(user.userName); //Charukirti

// 2. Array-like notaion ([])

// Syntax 👉 objectName['propertyName']

// Example :

console.log(user["age"]); //20
